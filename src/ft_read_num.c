#include "ft_printf.h"

int		ft_read_num(t_params *prms, int *j)
{
	int	num;

	num = 0;
	while (prms->s[*j] >= '0' && prms->s[*j] <= '9')
	{
		num = (num * 10) + (prms->s[*j] - '0');
		(*j)++;
	}
	(*j)--;
	return (num);
}
